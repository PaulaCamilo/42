int	ft_strcmp(char *s1, char *s2)
{
	int	p;

	p = 0;
	while (s1[p] == s2[p] && s1[p] != '\0' && s2[p] != '\0')
		p++;
	if (s1[p] == s2[p])
		return (0);
	else
		return (s1[p] - s2[p]);
}
