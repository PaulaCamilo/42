int	ft_str_is_printable(char *str)
{
	int	p;

	p = 0;
	if (str[p] == '\0')
	{
		return (1);
	}
	while (str[p] != '\0')
	{
		if (!(str[p] >= 32 && str[p] <= 126))
		{
			return (0);
		}
		p++;
	}
	return (1);
}
