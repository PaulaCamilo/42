char	*ft_strupcase(char *str)
{
	int	p;

	p = 0;
	while (str[p] != '\0')
	{
		if (str[p] >= 97 && str[p] <= 122)
		{
			str[p] -= 32;
		}
		p++;
	}
	return (str);
}
