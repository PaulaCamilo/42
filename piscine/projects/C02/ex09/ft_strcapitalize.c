char	*ft_strcapitalize(char *str)
{
	int	p;

	if (str[0] >= 'a' && str[0] <= 'z')
		str[0] -= 32;
	p = 1;
	while (str[p] != '\0')
	{
		if (str[p] >= 'A' && str[p] <= 'Z')
			str[p] += 32;
		if (str[p] >= 'a' && str[p] <= 'z')
		{
			if (!(str[p - 1] >= 'a' && str[p - 1] <= 'z'))
			{
				if (!(str[p - 1] >= '0' && str[p - 1] <= '9'))
				{
					if (!(str[p - 1] <= 'Z' && str[p - 1] >= 'A'))
						str[p] -= 32;
				}
			}
		}
		p++;
	}
	return (str);
}
