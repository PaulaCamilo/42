int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	p;

	p = 0;
	if (n == 0)
	{
		return (0);
	}
	while (s1[p] != '\0' && s2[p] != '\0' && p < n - 1 && s1[p] == s2[p])
	{
		p++;
	}
	if (s1[p] == s2[p])
	{
		return (0);
	}
	else
	{
		return (s1[p] - s2[p]);
	}
}
