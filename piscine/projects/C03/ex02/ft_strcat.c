char	*ft_strcat(char *dest, char *src)
{
	int	p;
	int	c;

	p = 0;
	while (dest[p] != '\0')
	{
		p++;
	}
	c = 0;
	while (src[c] != '\0')
	{
		dest[p] = src[c];
		p++;
		c++;
	}
	dest[p] = '\0';
	return (dest);
}
